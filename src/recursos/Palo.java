package recursos;

/**
 * Enumeration representing the four suits in a Spanish deck of cards.
 * Each suit is a distinct category: BASTOS, COPAS, ESPADAS, and OROS.
 */
public enum Palo {
    BASTOS, // Represents the suit of "Bastos" (clubs)
    COPAS, // Represents the suit of "Copas" (cups)
    ESPADAS, // Represents the suit of "Espadas" (swords)
    OROS // Represents the suit of "Oros" (coins)
}
